package pojo;

public class QuestionDetails {
	
	private String chapter;
	private String questions;
	private String choices;
	private String goldAnswer;
	private String silverAnswer;
	private String bronzeAnswer;
	
	public String getChapter() {
		return chapter;
	}
	public void setChapter(String chapter) {
		this.chapter = chapter;
	}
	public String getQuestions() {
		return questions;
	}
	public void setQuestions(String questions) {
		this.questions = questions;
	}
	public String getChoices() {
		return choices;
	}
	public void setChoices(String choices) {
		this.choices = choices;
	}
	public String getGoldAnswer() {
		return goldAnswer;
	}
	public void setGoldAnswer(String goldAnswer) {
		this.goldAnswer = goldAnswer;
	}
	public String getSilverAnswer() {
		return silverAnswer;
	}
	public void setSilverAnswer(String silverAnswer) {
		this.silverAnswer = silverAnswer;
	}
	public String getBronzeAnswer() {
		return bronzeAnswer;
	}
	public void setBronzeAnswer(String bronzeAnswer) {
		this.bronzeAnswer = bronzeAnswer;
	}
	

}
